   public void ValidarCodigo(){
       for(int i =0; i<Producto.getRowCount(); i++){
           if(Producto.getValueAt(i,0).equals(Codigo.getText())){
               JOptionPane.showMessageDialog(null,"Este codigo ya a sido ingresado");
               modelo.removeRow(i);
           }
       }
       
       Agregar();
   }
