    private void buscartodoCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_buscartodoCaretUpdate
        String valor = buscartodo.getText();

        if(valor.isEmpty()){
            Producto.clearSelection();
        }else{
            for(int i =0; i<Producto.getRowCount(); i++){
                if(Producto.getValueAt(i,0).equals(valor)){
                    //   Producto.requestFocus();
                    Producto.changeSelection(i, 0, false, false);

                }
            }
        }
    }//GEN-LAST:event_buscartodoCaretUpdate

    private void MOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MOActionPerformed
      
        int fila = Producto.getSelectedRow();

        if(fila >=0){
            Nombre.setText(Producto.getValueAt(fila,0).toString());
            Nombre.setText(Producto.getValueAt(fila,1).toString());
            Desc.setText(Producto.getValueAt(fila,2).toString());
            PrecioVenta.setText(Producto.getValueAt(fila,3).toString());
            PrecioCompra.setText(Producto.getValueAt(fila,4).toString());
            Fecha.setText(Producto.getValueAt(fila,5).toString());
            modelo.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }
    }//GEN-LAST:event_MOActionPerformed

