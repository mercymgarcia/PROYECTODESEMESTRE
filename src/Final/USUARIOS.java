    private void ELActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ELActionPerformed
        int fila = Bodega.getSelectedRow();
        if(fila >=0){
            modelo.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }
    }//GEN-LAST:event_ELActionPerformed

    private void MOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MOActionPerformed

        int fila = Bodega.getSelectedRow();

        if(fila >=0){
            Codigo.setText(Bodega.getValueAt(fila,0).toString());
            Nombre.setText(Bodega.getValueAt(fila,1).toString());
            Desc.setText(Bodega.getValueAt(fila,2).toString());
            VentaF.setText(Bodega.getValueAt(fila,3).toString());
            modelo.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }
    }//GEN-LAST:event_MOActionPerformed

