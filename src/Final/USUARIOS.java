public void validar(){
       if(Codigo.getText().equals("")){R1.setVisible(true);}else{R1.setVisible(false);}
       if(Nombre.getText().equals("")){R2.setVisible(true);}else{R2.setVisible(false);}
   } 
   public void ValidarCodigo(){
       for(int i =0; i<Bodega.getRowCount(); i++){
           if(Bodega.getValueAt(i,0).equals(Codigo.getText())){
               JOptionPane.showMessageDialog(null,"Este codigo ya a sido ingresado");
               modelo.removeRow(i);
           }
       }
       
       Agregar();
   }
   public void Agregar(){
       String Dato[] = new String[5];
      if( !(Codigo.getText().isEmpty())){
 Dato[0] = Codigo.getText();
 Dato[1]= Nombre.getText();
 Dato[2]= Desc.getText();
 Dato[3]= VentaF.getText();
 Dato[4]= Estado.getText();
 modelo.addRow(Dato);  
 Codigo.setText("");
 Nombre.setText("");
 Desc.setText("");
 Codigo.requestFocus();
      }else
       JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Atención", 2);

   }
